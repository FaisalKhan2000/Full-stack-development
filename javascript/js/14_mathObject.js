// Math Object

// The Math object in JavaScript provides a collection of mathematical functions and constants. It is not a constructor and does not have a Math() constructor function. Instead, it contains static methods and properties for performing mathematical operations.

// * Math.round(): Rounds a number to the nearest integer.
console.log(Math.round(4.7)); // Output: 5
console.log(Math.round(4.2)); // Output: 4

// * Math.floor(): Rounds a number downwards to the nearest integer.
console.log(Math.floor(4.7)); // Output: 4
console.log(Math.floor(4.2)); // Output: 4

// * Math.ceil(): Rounds a number upwards to the nearest integer.
console.log(Math.ceil(4.7)); // Output: 5
console.log(Math.ceil(4.2)); // Output: 5

// * Math.random() Generates a random floating-point number between 0 (inclusive) and 1 (exclusive).
console.log(Math.random()); // Output: (random number between 0 and 1)

// * Math.max() and Math.min()
// Math.max() returns the largest of zero or more numbers.
// Math.min() returns the smallest of zero or more numbers.
console.log(Math.max(10, 20, 5)); // Output: 20
console.log(Math.min(10, 20, 5)); // Output: 5

// * Math.pow(): Raises a number to a specified power.
console.log(Math.pow(2, 3)); // Output: 8 (2 raised to the power of 3)

// * Constants in Math Object:

// * Math.PI: Represents the ratio of the circumference of a circle to its diameter.
console.log(Math.PI); // Output: 3.141592653589793

// * Math.E: Represents Euler's number, the base of natural logarithms.
console.log(Math.E); // Output: 2.718281828459045
